# âœ… GET /metrics/{model_id} â†’ Specific Model
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
@app.get("/metrics/{model_id}")
def get_model_metrics(model_id: str):
    logging.info(f"[MMS-API] ğŸ” Fetching metrics for model: {model_id}")
    raw = r.get(f"metrics:{model_id}")
    if raw:
        try:
            return json.loads(raw)
        except Exception as e:
            logging.error(f"[MMS-API] âŒ JSON decode error for {model_id}: {e}")
            return {"error": "Failed to decode metrics"}
    else:
        return {"error": "Model not found"}
