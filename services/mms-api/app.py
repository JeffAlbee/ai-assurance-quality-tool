@app.get("/metrics")
def get_all_metrics():
    logging.info("[MMS-API] ğŸ” Fetching all model metrics from Redis")
    all_keys = r.keys("metrics:*")
    all_metrics = {}

    for key in all_keys:
        model_id = key.split(":")[1]
        raw = r.get(key)
        try:
            all_metrics[model_id] = json.loads(raw)
        except Exception as e:
            logging.warning(f"[MMS-API] âš ï¸ Failed to parse metrics for {model_id}: {e}")

    return all_metrics

